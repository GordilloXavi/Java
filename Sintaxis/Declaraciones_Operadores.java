
public class Declaraciones_Operadores {

	public static void main(String[] args) {
		
		//int c = 5;
		
		//final int d = 6;
		/* La palabra final delante de una variable la convierte en una 
		 * constante ésta constante ya no se podrá modificar.
		 */
		
		
		
		final double a_pulgadas;
		
		double centimetros;
		
		centimetros = 6; 
		
		a_pulgadas = 2.54;
		
		double resultado = centimetros / a_pulgadas;
		
		
		
		System.out.println("En " + centimetros + " centímetros hay " + resultado + " pulgadas");
		
		
		
		
		
	}

}

